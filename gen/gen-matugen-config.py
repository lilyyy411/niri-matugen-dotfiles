import glob
import sys
from pathlib import Path


def main(argv) -> int:
    files = [
        i
        for i in map(
            Path, glob.glob(f"{argv[2]}/**/*", recursive=True, include_hidden=True)
        )
        if i.is_file()
    ]
    # print(*files)
    with open(argv[1], "w+") as f:
        print("# Automatically generated by gen-matugen.py", file=f)
        #         print(
        #             """[config]
        # version_check = false
        # """,
        #             file=f,
        #         )

        for file in files:
            print(f'[templates."{file}"]', file=f)
            stripped = Path(*file.parts[1:])
            # if os.
            print(f'output_path = "~/{stripped}"', file=f)
            print(f'input_path = "{file.absolute()}"', file=f)
            print(file=f)

        return 0
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv))
