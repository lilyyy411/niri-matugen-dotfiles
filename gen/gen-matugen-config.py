import glob
import sys
from pathlib import Path


def main(argv) -> int:
    files = [
        i
        for i in map(
            Path, glob.glob(f"{argv[2]}/**/*", recursive=True, include_hidden=True)
        )
        if i.is_file()
    ]
    for i in argv[3:]:
        files.extend(
            (
                file
                for file in map(Path, glob.glob(i, recursive=True, include_hidden=True))
                if file.is_file()
            )
        )
    # print(*files)
    with open(argv[1], "w+") as f:
        print("# Automatically generated by gen-matugen-config.py", file=f)
        #         print(
        #             """[config]
        # version_check = false
        # """,
        #             file=f,
        #         )
        home = str(Path("~").expanduser())
        for file in files:
            print(f'[templates."{file}"]', file=f)
            stripped = (
                str(file).replace(home, "")
                if home in str(file)
                else Path(*file.parts[1:])
            )
            print(f'output_path = "~/{stripped}"', file=f)
            print(f'input_path = "{file.absolute()}"', file=f)
            print(file=f)

        return 0
    return 0


if __name__ == "__main__":
    raise SystemExit(main(sys.argv))
